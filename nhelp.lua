function stringToAnagramma(str)
	local tab = {}
	for i = 1, #str do
		tab[i] = str:sub(i, i)
	end
    local n, order, res = #tab, {}, {} 
    for i=1,n do order[i] = { rnd = math.random(), idx = i } end 
    table.sort(order, function(a,b) return a.rnd < b.rnd end) 
    for i=1,n do res[i] = tab[order[i].idx] end 
	local strings = ""
	for k, v in pairs(res) do
		if cfg.anagrammator.Points then v = v.."." end
		strings = string.format("%s%s", strings, v)
	end
	if cfg.anagrammator.Register == 0 then
		return strings
	elseif cfg.anagrammator.Register == 1 then
		return string.rupper(strings)
	elseif cfg.anagrammator.Register == 2 then
		return string.rlower(strings)
	end
