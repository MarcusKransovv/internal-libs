copas.addthread(function()
    local response = {}
    local _, code = https.request{
        url = "https://raw.githubusercontent.com/MarcusKransovv/internal-libs/main/changelog.json",
        sink = ltn12.sink.table(response),
        protocol = "tlsv1_2"
    }

    if code == 200 then
        local body = table.concat(response)
        local ver = body:match([["latest"%s*:%s*"([%d%.]+)"]])
        if ver and ver ~= latest_version then
            latest_version = ver
            new_version_available = true
        end
    end

    local body = table.concat(response)
    local ver = body:match([["latest"%s*:%s*"([%d%.]+)"]])
    if ver and ver ~= latest_version then
        latest_version = ver
        new_version_available = true
    end

-- Распарсим весь JSON
local json = require("json")
local data = json.decode(body)
if data then
    changelog = data.changelog
    changelog_ready = true
end

end)

