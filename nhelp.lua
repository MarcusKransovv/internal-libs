function loadLib(lib_data)
    local dlstatus = require('moonloader').download_status
    local loadPath = ''
    
    print('[loadLib] Ќачинаем загрузку: ' .. lib_data.name)

    if lib_data.name == 'imgui_notf' then
        loadPath = getWorkingDirectory()..'\\'
    elseif lib_data.folder ~= '' then
        loadPath = getWorkingDirectory()..'\\lib\\'..lib_data.folder..'\\'
        if not doesDirectoryExist(loadPath) then
            createDirectory(loadPath)
            print('[loadLib] ѕапка "'..loadPath..'" создана')
        else
            print('[loadLib] ѕапка "'..loadPath..'" уже существует')
        end
    else
        loadPath = getWorkingDirectory()..'\\lib\\'
    end

    -- ѕроверка наличи€ всех файлов
    local totalFiles = #lib_data.files
    local exists = 0

    for i, file in ipairs(lib_data.files) do
        local filePath = loadPath .. file.name
        if doesFileExist(filePath) then
            print('[loadLib] Ќайден: ' .. file.name)
            exists = exists + 1
        else
            print('[loadLib] ќтсутствует: ' .. file.name)
        end
    end

    -- ≈сли не все файлы есть Ч загружаем недостающие
    if exists ~= totalFiles then
        print('[loadLib] «агружаем недостающие файлы...')
        for i, file in ipairs(lib_data.files) do
            local filePath = loadPath .. file.name
            if not doesFileExist(filePath) then
                print('[loadLib] —качиваем: ' .. file.name)
                downloadUrlToFile(file.link, filePath, function(id, status, p1, p2)
                    if status == dlstatus.STATUS_DOWNLOADINGDATA then
                        print('[loadLib] «агрузка ' .. file.name .. ': ' .. math.floor(p1 / p2 * 100) .. '%')
                    elseif status == dlstatus.STATUSEX_ENDDOWNLOAD then
                        print('[loadLib] ”спешно загружено: ' .. file.name)
                    elseif status == dlstatus.STATUSEX_ERROR then
                        print('[loadLib] ќшибка загрузки: ' .. file.name)
                    end
                end)
            end
        end
    else
        print('[loadLib] ¬се файлы уже присутствуют. «агрузка не требуетс€.')
    end
end

function splitLines(str_or_table)
    local t = {}

    if type(str_or_table) == "string" then
        for line in str_or_table:gmatch("[^\n]+") do
            line = line:gsub("^%s*(.-)%s*$", "%1")
            if line ~= "" then table.insert(t, line) end
        end
    elseif type(str_or_table) == "table" then
        for _, line in ipairs(str_or_table) do
            line = line:gsub("^%s*(.-)%s*$", "%1")
            if line ~= "" then table.insert(t, line) end
        end
    end

    return t
end


